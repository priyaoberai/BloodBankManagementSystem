    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String donorId= jLabel3.getText();
        String name= jTextField1.getText();
        String fatherName= jTextField2.getText();
        String motherName= jTextField3.getText();
//        SimpleDateFormat dFormat= new SimpleDateFormat("MM-dd-yyyy", Locale.ENGLISH);
//        String DOB= dFormat.format(jDateChooser1.getDate());
        
        String DOB = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();

        String MobileNo= jTextField4.getText();
        String gender= (String)jComboBox1.getSelectedItem();
        String email= jTextField5.getText();
        String bloodGroup= (String)jComboBox2.getSelectedItem().toString();
        String city= jTextField6.getText();
        String address= jTextArea1.getText();
        //String lastDonated= dFormat.format(jDateChooser2.getDate());
        String lastDonated = ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
        String donateStatus= (String)jComboBox3.getSelectedItem();
        
        if(name.trim().isEmpty() || DOB == null || MobileNo.trim().isEmpty() || gender.trim().isEmpty() || email.trim().isEmpty() || bloodGroup.trim().isEmpty() || city.trim().isEmpty() || lastDonated == null || donateStatus.trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Enter Your Details Properly! If you'll not fill all the fields, you'll not be able to Register.", "Error", JOptionPane.ERROR_MESSAGE);
            
            //jLabel15.setText("Please Enter Your Details Properly! If you'll not fill all the fields, you'll not be able to Register.");
            
        } else{
        
        try{
           Connection con= ConnectionProvider.getCon();
           Statement st= con.createStatement();
           st.executeUpdate("insert into donor values('"+donorId+"', '"+name+"', '"+fatherName+"', '"+motherName+"', '"+DOB+"', '"+MobileNo+"', '"+gender+"', '"+email+"', '"+bloodGroup+"', '"+city+"', '"+address+"', '"+lastDonated+"', '"+donateStatus+"'  )");
           
           JOptionPane.showMessageDialog(null, "Successfully Updated!");
           setVisible(false);
           new addNewDonor().setVisible(true);
           }
        catch(Exception e){
                   JOptionPane.showMessageDialog(null, e);
                         }
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

